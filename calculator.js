// Define the numbers and the operation here
const num1 = 10;
const num2 = 5;
const operation = "";
// Write if-statements here
if (operation === "+") {
  console.log(`${num1} + ${num2} = ${num1 + num2}`);
} else if (operation === "-") {
  console.log(`${num1} - ${num2} = ${num1 - num2}`);
} else if (operation === "*") {
  console.log(`${num1} * ${num2} = ${num1 * num2}`);
} else {
  console.log(`${num1} / ${num2} = ${num1 / num2}`);
}
